    private ArrayList<Hitbox> hitboxBlocsSolides;
    private final int TAILLE_BLOC = 32;

    public Terrain() {
        hitboxBlocsSolides = new ArrayList<>();

        for (int y = 0; y < map.length; y++) {
            for (int x = 0; x < map[0].length; x++) {
                int val = map[y][x];
                if (val == 1 || val == 2) {
                    // crée une hitbox pour ce bloc solide
                    int posX = x * TAILLE_BLOC;
                    int posY = y * TAILLE_BLOC;
                    hitboxBlocsSolides.add(new Hitbox(posX, posY, TAILLE_BLOC, TAILLE_BLOC));
                }
            }
        }
    }

    public void mettreAJourHitboxBlocsSolides() {
        hitboxBlocsSolides.clear();
        int[][] map = getMap();

        for (int y = 0; y < map.length; y++) {
            for (int x = 0; x < map[0].length; x++) {
                if (map[y][x] == 1 || map[y][x] == 2) { // blocs solides
                    // Créer une hitbox pour ce bloc
                    Hitbox hb = new Hitbox(x * TAILLE_BLOC, y * TAILLE_BLOC, TAILLE_BLOC, TAILLE_BLOC);
                    hitboxBlocsSolides.add(hb);
                }
            }
        }
    }



